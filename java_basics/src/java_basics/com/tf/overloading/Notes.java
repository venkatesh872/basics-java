/*
 * Method overloading
 * Constructor overloading 
 * this keyword 
 * static keyword
 * --------------------------------------
 * Methodoverloading-->A class contains more than one method with the
 *             same name is called as method overloading.
 * constructor overloading--->A class contains more than one constructor 
 *              is called as constructor overloading.
 *              
 *              
 * Rules:
 * 1)number of parameters should be different
 * 2)order of parameters
 * 3)datatype of parameters  
 * 
 * Constructor
 * ---------------------
 * 1) constructor is a special kind of method.
 * 2)constructor name should always be same as class name.
 * 3)constructor will not return any value.
 * 4)constructor will be invoke at the time object creation.(we no need to call the constructor explicitely)
 * 5)constructor will take the parameters(just like method)
 * 6)constructor is used for initialize the values.
 *
 *
 *two types of constructors:
 *-----------------------------------
 *1)default constructor
 *2)parameterized constructor
 */